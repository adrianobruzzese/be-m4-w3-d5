package com.epicode.entities;

/**
 * Enumerazione che rappresenta la frequenza di pubblicazione per le riviste.
 * Questi valori possono essere utilizzati per descrivere quanto frequentemente una rivista viene pubblicata.
 */
public enum PublicationFrequency {
    WEEKLY,      // Pubblicazione settimanale
    MONTHLY,     // Pubblicazione mensile
    HALF_YEARLY,  // Pubblicazione semestrale
    YEARLY,     // Pubblicazione mensile
}
